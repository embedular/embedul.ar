static int assortedCompare (const void *g1, const void *g2)
{
    return (int)((const struct VIDEO_FONT_AssortedGlyph *)g1)->code
         - (int)((const struct VIDEO_FONT_AssortedGlyph *)g2)->code;
}


static const uint8_t (* assorted (uint16_t codepoint))[8]
{
    const struct VIDEO_FONT_AssortedGlyph *ag;

    ag = (const struct VIDEO_FONT_AssortedGlyph *)
                 bsearch (&codepoint, s_Assorted,
                             sizeof(s_Assorted) / sizeof(s_Assorted[0]),
                             sizeof(s_Assorted[0]),
                             assortedCompare);
    if (ag)
    {
        return &ag->glyph;
    }

    return NULL;
}


void VIDEO_FONT_STD (struct VIDEO_FONT *const F)
{
    if (!F)
    {
        return;
    }
    
    F->basicLatin       = &s_BasicLatin;
    F->latin1           = &s_Latin1;
    F->greek            = &s_Greek;
    F->boxDrawing       = &s_BoxDrawing;
    F->blockElements    = &s_BlockElements;
    F->cjkSymbols       = &s_CjkSymbols;
    F->hiragana         = &s_Hiragana;
    F->katakana         = &s_Katakana;
    F->assorted         = assorted;
    F->missing          = NULL;
}
